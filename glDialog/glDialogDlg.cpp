// CglDialogDlg 消息处理程序

BOOL CglDialogDlg::OnInitDialog()
{
	CDialogEx::OnInitDialog();

	// 将“关于...”菜单项添加到系统菜单中。

	// IDM_ABOUTBOX 必须在系统命令范围内。
	ASSERT((IDM_ABOUTBOX & 0xFFF0) == IDM_ABOUTBOX);
	ASSERT(IDM_ABOUTBOX < 0xF000);

	CMenu* pSysMenu = GetSystemMenu(FALSE);
	if (pSysMenu != NULL)
	{
		BOOL bNameValid;
		CString strAboutMenu;
		bNameValid = strAboutMenu.LoadString(IDS_ABOUTBOX);
		ASSERT(bNameValid);
		if (!strAboutMenu.IsEmpty())
		{
			pSysMenu->AppendMenu(MF_SEPARATOR);
			pSysMenu->AppendMenu(MF_STRING, IDM_ABOUTBOX, strAboutMenu);
		}
	}

	// 设置此对话框的图标。当应用程序主窗口不是对话框时，框架将自动
	//  执行此操作
	SetIcon(m_hIcon, TRUE);			// 设置大图标
	SetIcon(m_hIcon, FALSE);		// 设置小图标

	// TODO: 在此添加额外的初始化代码
	m_Display.Create( NULL,   //缺省的窗口
		NULL,   //无窗口名称
		WS_CHILD|WS_CLIPSIBLINGS|WS_CLIPCHILDREN|WS_VISIBLE,
		// 定义窗口风格
		CRect(0, 0, 256, 256),   // 窗口的大小
		this,   // 指定当前对话框为其父窗口指针
		0); 

	m_Display.initialize();

	m_Display.OpenIOI( "./0411.ioi" ) ;
	return TRUE;  // 除非将焦点设置到控件，否则返回 TRUE
}

void CglDialogDlg::OnSysCommand(UINT nID, LPARAM lParam)
{
	if ((nID & 0xFFF0) == IDM_ABOUTBOX)
	{
		CAboutDlg dlgAbout;
		dlgAbout.DoModal();
	}
	else
	{
		CDialogEx::OnSysCommand(nID, lParam);
	}
}

// 如果向对话框添加最小化按钮，则需要下面的代码
//  来绘制该图标。对于使用文档/视图模型的 MFC 应用程序，
//  这将由框架自动完成。

void CglDialogDlg::OnPaint()
{
	if (IsIconic())
	{
		CPaintDC dc(this); // 用于绘制的设备上下文

		SendMessage(WM_ICONERASEBKGND, reinterpret_cast<WPARAM>(dc.GetSafeHdc()), 0);

		// 使图标在工作区矩形中居中
		int cxIcon = GetSystemMetrics(SM_CXICON);
		int cyIcon = GetSystemMetrics(SM_CYICON);
		CRect rect;
		GetClientRect(&rect);
		int x = (rect.Width() - cxIcon + 1) / 2;
		int y = (rect.Height() - cyIcon + 1) / 2;

		// 绘制图标
		dc.DrawIcon(x, y, m_hIcon);
	}
	else
	{
		CDialogEx::OnPaint();
	}
}

//当用户拖动最小化窗口时系统调用此函数取得光标
//显示。
HCURSOR CglDialogDlg::OnQueryDragIcon()
{
	return static_cast<HCURSOR>(m_hIcon);
}


std::string CglDialogDlg::getFileFromDialog( std::string ext )
{
	std::string DefExt;

	std::string filepath;


	DefExt = ext + "文件(*." + ext + ")|*." + ext + "|)";
	// DefExt.Format("%s","mod文件(*.mod)|*.mod|");

	///CFileDialog第一个参数若为true，为打开文件对话框,该段基于mfc
	CFileDialog dlgmodelFile(true,
		ext.c_str(),NULL,
		OFN_HIDEREADONLY|OFN_CREATEPROMPT|OFN_NONETWORKBUTTON,
		DefExt.c_str() );

	dlgmodelFile.m_ofn.lpstrTitle="打开";

	if(dlgmodelFile.DoModal()==IDOK)
	{
		filepath = dlgmodelFile.GetPathName();
	}

	return filepath;
}
